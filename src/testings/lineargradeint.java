    public final void buttongradient() {
    btn.setContentAreaFilled(false); // important
    btn.setOpaque(false);             // allow gradient painting

    btn = new javax.swing.JButton(btn.getText()) { // optional if replacing text
        @Override
        protected void paintComponent(Graphics g) {
            Graphics2D g2d = (Graphics2D) g;
            int w = getWidth();
            int h = getHeight();
            GradientPaint gp = new GradientPaint(0, 0, Color.GREEN, w, h, Color.WHITE);
            g2d.setPaint(gp);
            g2d.fillRect(0, 0, w, h);
            super.paintComponent(g);
        }
    };
}



