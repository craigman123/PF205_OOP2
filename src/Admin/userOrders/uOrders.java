    public void countIndicators() {
        config conf = new config();
        try {
            // 1️⃣ Count pending orders
            String pendingQuery = "SELECT COUNT(*) AS cnt FROM userOrders WHERE order_status = 'Pending'";
            java.util.List<java.util.Map<String, Object>> pendingResult = conf.fetchRecords(pendingQuery);

            int pendingCount = 0;
            if (!pendingResult.isEmpty()) {
                pendingCount = Integer.parseInt(pendingResult.get(0).get("cnt").toString());
            }

            // 2️⃣ Count total orders
            String totalQuery = "SELECT COUNT(*) AS cnt FROM userOrders";
            java.util.List<java.util.Map<String, Object>> totalResult = conf.fetchRecords(totalQuery);

            int totalCount = 0;
            if (!totalResult.isEmpty()) {
                totalCount = Integer.parseInt(totalResult.get(0).get("cnt").toString());
            }

            // 3️⃣ Update labels
            pending.setText(String.valueOf(pendingCount) + " Pending Orders");
            pending.setForeground(Color.decode("#006307"));
            orders.setText(String.valueOf(totalCount) + " Orders");
            orders.setForeground(Color.decode("#876900"));

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
