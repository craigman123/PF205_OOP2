    public void disp(){
        String stat = (String) status.getSelectedItem();
        String search = (String) searchBox.getSelectedItem();
        
        if(search.equals("Order ID")){
            search = "order_id";
        }else if (search.equals("Product ID")) {
            search = "prod_id";
        }else if (search.equals("User ID")) {
            search = "user_id";
        }
        
        DisplayTable table = new DisplayTable(stat, search, searchText.getText(), miniPanel);
        MainPane.remove(table);
        MainPane.add(table).setVisible(true);
    }

